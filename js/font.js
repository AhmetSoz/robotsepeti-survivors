const CHAR_W = 6; // 5 piksel glif + 1 boşluk

function textW(txt, scale) {
  scale = scale || 1;
  return String(txt).length * CHAR_W * scale - scale;
}

// Metni piksel font ile çizer. opts: {scale, align, shadow, alpha}
function drawText(ctx, txt, x, y, color, opts) {
  opts = opts || {};
  const s = opts.scale || 1;
  txt = String(txt).toLocaleUpperCase('tr-TR');
  const w = textW(txt, s);
  if (opts.align === 'center') x -= w / 2;
  else if (opts.align === 'right') x -= w;
  x = Math.round(x); y = Math.round(y);
  if (opts.alpha !== undefined) { ctx.save(); ctx.globalAlpha = opts.alpha; }
  const passes = opts.shadow ? [[opts.shadow, s], [color, 0]] : [[color, 0]];
  for (const [col, off] of passes) {
    ctx.fillStyle = col;
    let cx = x;
    for (let i = 0; i < txt.length; i++) {
      const g = FONT[txt[i]];
      if (g) {
        for (let r = 0; r < 7; r++) {
          const bits = g[r];
          if (!bits) continue;
          for (let c = 0; c < 5; c++) {
            if (bits & (16 >> c)) ctx.fillRect(cx + c * s, y + off + r * s, s, s);
          }
        }
      }
      cx += CHAR_W * s;
    }
  }
  if (opts.alpha !== undefined) ctx.restore();
  return w;
}
